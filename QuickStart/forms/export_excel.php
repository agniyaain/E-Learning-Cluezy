    <meta charset="UTF-8">
    <style>
        table { border-collapse: collapse; width: 100%; }
        th, td { border: 1px solid black; padding: 8px; text-align: left; }
        th { background-color: #f2f2f2; }
        .title { font-size: 18px; font-weight: bold; margin-bottom: 10px; }
    </style>
</head>
<body>
    <div class="title">Hasil Quiz:                                                                                                       <?php echo $quiz['judul']; ?></div>
    <div>Kode Session:                                                                   <?php echo $session['kode_session']; ?></div>
    <div>Tanggal Export:                                                                         <?php echo date('d/m/Y H:i'); ?></div>
    <br>

    <table>
        <thead>
            <tr>
                <th>No</th>
                <th>Username</th>
                <th>Email</th>
                <th>Nilai</th>
                <th>Status</th>
                <th>Waktu Bergabung</th>
            </tr>
        </thead>
        <tbody>
            <?php
                $no = 1;
                while ($row = mysqli_fetch_assoc($peserta)):
            ?>
            <tr>
                <td><?php echo $no++; ?></td>
                <td><?php echo htmlspecialchars($row['username']); ?></td>
                <td><?php echo htmlspecialchars($row['email']); ?></td>
                <td>
                    <?php
                        if ($row['nilai'] !== null) {
                            echo $row['nilai'];
                        } else {
                            echo 'Belum Mengerjakan';
                        }
                    ?>
                </td>
                <td>
                    <?php
                        if ($row['nilai'] !== null) {
                            echo 'Selesai';
                        } else {
                            echo 'Menunggu';
                        }
                    ?>
                </td>
                <td><?php echo date('d/m/Y H:i', strtotime($row['waktu_join'])); ?></td>
            </tr>
            <?php endwhile; ?>

            <?php if (mysqli_num_rows($peserta) == 0): ?>
            <tr>
                <td colspan="6" style="text-align: center;">Tidak ada peserta</td>
            </tr>
            <?php endif; ?>
        </tbody>
    </table>

