<?php
    session_start();
    include "koneksi.php";

    $id_session = $_GET['id_session'] ?? 0;

    // Cek session dan ownership
    $session = mysqli_fetch_assoc(mysqli_query($koneksi, "SELECT * FROM quiz_session WHERE id_session='$id_session'"));
    if (! $session) {
        die("Session tidak ditemukan");
    }

    // Cek apakah user adalah owner session
    if ($session['id_user'] != $_SESSION['id_user']) {
        die("Hanya owner session yang bisa export hasil");
    }

    // Ambil data quiz
    $quiz = mysqli_fetch_assoc(mysqli_query($koneksi, "SELECT * FROM quiz WHERE id_quiz='{$session['id_quiz']}'"));

    // Ambil data peserta dengan nilai
    $peserta = mysqli_query($koneksi, "
    SELECT u.username, u.email, us.nilai, us.waktu_join
    FROM user_session us
    JOIN user u ON us.id_user = u.id_user
    WHERE us.id_session='$id_session'
    ORDER BY us.nilai DESC
");

    // Set headers untuk download Excel
    header("Content-Type: application/vnd.ms-excel");
    header("Content-Disposition: attachment; filename=\"Hasil_Quiz_{$quiz['judul']}_" . date('Y-m-d') . ".xls\"");
    header("Pragma: no-cache");
    header("Expires: 0");
?>

